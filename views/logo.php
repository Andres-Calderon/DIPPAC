Basados en esta idea nace el propósito del proyecto Detección de problemas de aprendizaje en el aula de clase  (DPAAC), en donde se busca abordar niños en etapa de escolarización con el fin de estudiar sus procesos de aprendizaje e identificar aquellas dificultades que se puedan presentar, esto con el fin de brindar la información necesaria que permita tratar dichos aspectos y favorecer el propósito de la etapa escolar. 

              </p>
