              <p>
              El desarrollo del niño es una de las etapas más importantes, no sólo para la construcción de la personalidad y la conformación de esquemas relacionales sino para la edificación de la identidad. Dentro de estos procesos la escuela cumple papel fundamental, ya que es uno de los contextos primarios donde emergen procesos de aprendizaje que permiten la interacción del individuo con su medio.  
              </p>
              <p>
              Es tal la importancia de la escuela que a nivel legal se le ha otorgado la responsabilidad de contribuir con la prevención de hechos que puedan no favorecer el desarrollo del niño tanto a nivel emocional como en sus procesos cognitivos.  La facultad de educación de la Universidad de La Sabana realizó un estudio en el año 2013, cuyos resultados arrojaron que el <b>93% de las dificultades de lecto-escritura pueden ser identificadas y tratadas dentro del aula de clase</b>.  Además concluye que una de las razones más frecuentes de deserción en los años iniciales de escolarización tiene que ver precisamente con la falta de comprensión de los estudiantes con los temas presentados en clase. 
